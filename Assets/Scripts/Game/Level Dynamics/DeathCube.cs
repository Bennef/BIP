using UnityEngine;
// Fall death.
public class DeathCube : MonoBehaviour 
{
    // ----------------------------------------------- Data members ----------------------------------------------


    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    void OnTriggerEnter(Collider other)
	{
		if (other.gameObject.name == "Bip") 
		{
            other.GetComponent<CharacterController>().StartFallDeath();
		}
	}
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
