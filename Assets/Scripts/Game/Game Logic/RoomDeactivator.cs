using UnityEngine;

public class RoomDeactivator : MonoBehaviour
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public GameObject[] roomsToDeactivate;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    // Use this for initialization
    void OnEnable()
    {
		foreach (GameObject room in roomsToDeactivate)
        {
            room.SetActive(false);
        }
	}
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
