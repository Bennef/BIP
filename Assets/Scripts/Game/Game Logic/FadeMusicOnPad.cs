using UnityEngine;

public class FadeMusicOnPad : MonoBehaviour
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public AudioSource darkScary;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    private void OnTriggerEnter(Collider other)
    {
        if (other.name == "Bip")
        {
            StartCoroutine(AudioFadeOut.FadeOut(darkScary, 2.0f));
        }
    }
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}

