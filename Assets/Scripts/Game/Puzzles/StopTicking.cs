using UnityEngine;

public class StopTicking : MonoBehaviour
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public LaserRunner laserRunner;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    private void OnTriggerEnter(Collider other)
    {
        if (other.name == "Bip")
        {
            laserRunner.EndSequence();
        }
    }
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
