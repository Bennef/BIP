using UnityEngine;

public class TargetTile : MonoBehaviour
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public bool isCorrect = false;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    private void OnTriggerEnter(Collider other)
    {
        if (other.tag == "Moveable")
        {
            isCorrect = true;
        }
    }
    // --------------------------------------------------------------------
    private void OnTriggerExit(Collider other)
    {
        if (other.tag == "Moveable")
        {
            isCorrect = false;
        }
    }
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
