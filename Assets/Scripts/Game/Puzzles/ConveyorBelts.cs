using UnityEngine;

public class ConveyorBelts : MonoBehaviour
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public Conveyor[] platforms;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    public void SwitchOn()
    {
        foreach (Conveyor platform in platforms)
        {
            platform.isOn = true;
        }
    }
    // --------------------------------------------------------------------
    public void SwitchOff()
    {
        foreach (Conveyor platform in platforms)
        {
            platform.isOn = false;
        }
    }
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
