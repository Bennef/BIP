using UnityEngine;

public class ResetAlarm : MonoBehaviour
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public TripLaserPuzzle tripLaserPuzzle;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    public void OnTriggerEnter(Collider other)
    {
        if (other.name == "Collider front")
        {
            tripLaserPuzzle.ResetCo();
        }
    }
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
