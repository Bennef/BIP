using UnityEngine;

public class LookAtCamera : MonoBehaviour 
{
    // ----------------------------------------------- Data members ----------------------------------------------
    public Camera mainCam;
    // ----------------------------------------------- End Data members ------------------------------------------

    // --------------------------------------------------- Methods -----------------------------------------------
    // --------------------------------------------------------------------
    void Start()
    {
        mainCam = Camera.main;
    }
    // --------------------------------------------------------------------
    void Update()
    {
        if (mainCam == null)
        {
            mainCam = GameObject.Find("Main Camera").GetComponent<Camera>();
        }
        transform.LookAt(mainCam.transform);
    }
    // --------------------------------------------------------------------
    // --------------------------------------------------- End Methods --------------------------------------------
}
